def get_rankings():
    query = '''
        SELECT u.full_name, COUNT(r.id) as total_wins
        FROM users u
        LEFT JOIN rankings r ON u.id = r.user_id
        WHERE u.is_approved = 1 AND u.is_admin = 0
        GROUP BY u.id, u.full_name
        ORDER BY total_wins DESC
    '''
    return safe_query(query)

